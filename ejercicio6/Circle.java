public class Circle {
    private double radius;
    private double diameter;
    private double area;

    public Circle(){
        this.radius = radius;
    }
    
    public double diametro(){
        return diameter =radius*2.0;
    }
    public double area (){
      return Math.PI*radius*radius;  
    }
    public void setRadius(double radius) {
        this.radius = radius;
    }

    public double getRadius() {
        return radius;
    }
    
    
}
